function createConfigTableFromClipboard()
  clipboard = reaper.CF_GetClipboard('')
  
  configTable = {}
  -- Iterate through each line and insert it into the table
  for line in clipboard:gmatch("[^\r\n\t]+") do
      table.insert(configTable, line)
  end
end

